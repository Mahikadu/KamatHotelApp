    protected synchronized void showProgressDialog(String message) {
        if (progressDialogFragment != null && progressDialogFragment.isVisible()) {
            // Before creating another loading dialog, close all opened loading dialogs (if any)
            progressDialogFragment.dismissAllowingStateLoss();
        }
        progressDialogFragment = null;
        progressDialogFragment = new ProgressDialogFragment(message);
        FragmentManager fm = getFragmentManager();
        progressDialogFragment.show(fm, ProgressDialogFragment.class.toString());
    }

    protected synchronized void dismissDialog() {
        progressDialogFragment.dismissAllowingStateLoss();
    }


    /* TODO Store Crop Image Path */
    public String storeProfileImage(Bitmap profileBitmap,String selprofilePhoto) {
        String profileImagePath = "";
        String fname =  selprofilePhoto +"_" + System.currentTimeMillis() + ".jpeg";
        File root = new File(Environment.getExternalStorageDirectory() + File.separator + "scanCrop" + File.separator);
        root.mkdirs();

        root.setWritable(true);

        File profileFile = new File(root.getPath(), fname);
        FileOutputStream fos = null;
        try {
            if (!TextUtils.isEmpty(fname)) {

                fos = new FileOutputStream(profileFile);
                profileBitmap.compress(Bitmap.CompressFormat.PNG, 90, fos);
                fos.close();
                fos.flush();

//                profileImagePath = Environment.getExternalStorageDirectory() + "/" + Utils.CAMERA_CAPTURE_FOLDER_NAME + "/" + fname;
                profileImagePath = profileFile.toString();
//                Toast.makeText(this, "Crop Image Path : " + profileImagePath, Toast.LENGTH_SHORT).show();
            }

        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
        return profileImagePath;

    }
